    public static boolean isZSF(int[]pivots){
        boolean ret=true;
        int start=pivots[0];
        for(int i=1;i<pivots.length;i++){
            if(pivots[i]>=start)ret=false;
            start=pivots[i];
        }
        return ret;
    }
    
    
    public static int pivotNumberOfVec(Vector<Double> vec){
        int ret=0;
        for(int i=0;i<vec.size();i++)if(vec.elementAt(i)==0&&i<vec.size()-1)ret=ret+1;
        return ret;
    }
