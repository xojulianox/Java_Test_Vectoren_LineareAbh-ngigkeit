    public static Vector<Vector<Double>> swapLines(Vector<Vector<Double>> vec){
        int m =vec.size();
        int n =vec.elementAt(0).size();
        
        for(int s=0;s<n;s++){
        
        for(int j=0;j<m;j++){
        int indexc=j;
        for(int i=j;i<n;i++){
            if (vec.elementAt(i).elementAt(s)!=0)vec=swap(vec,i,indexc);
            indexc++;
        }}}
        
        return vec;
    }
    
    public static Vector<Vector<Double>> swap(Vector<Vector<Double>> vec, int a,int b){
        Vector<Double> alpha=vec.elementAt(a);
        Vector<Double> beta=vec.elementAt(b);
        
        vec.set(a, beta);
        vec.set(b,alpha);
        
        return vec;
    }
   
    
